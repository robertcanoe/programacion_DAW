"""
Realiza un programa que pida el día de la semana (del 1 al 7) y escriba el día correspondiente.
Si introducimos otro número nos da un error.

Autor: Roberto Cano Estévez
Fecha: 19/10/2024
"""
print("Este programa solicita un número y devuelve el día de la semana correspondiente.")
print("--------------------------------------------------------------------------------")


day = int(input("Introduce un número del 1 al 7 para indicar el día de la semana: "))

if day == 1:
    print("Lunes")
elif day == 2:
    print("Martes")
elif day == 3:
    print("Miércoles")
elif day == 4:
    print("Jueves")
elif day == 5:
    print("Viernes")
elif day == 6:
    print("Sábado")
elif day == 7:
    print("Domingo")
else:
    print("Error: Número no válido, introduce un número entre 1 y 7.")




